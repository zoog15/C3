using System;
using static System.Console;

namespace C3_Practice
{
    class RelationalOperator
    {
        static void Main(string[] args) {
            Console.WriteLine($"3 > 4 : {3 > 4}");
            Console.WriteLine($"3 >= 4 : {3 >= 4}");
            Console.WriteLine($"3 < 4 : {3 < 4}");
            Console.WriteLine($"3 <= 4 : {3 <= 4}");
            Console.WriteLine($"3 == 4 : {3 == 4}");
            Console.WriteLine($"3 != 4 : {3 != 4}");
        }
    }
}
