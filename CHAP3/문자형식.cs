using System;

namespace C3_Practice
{
    class Char
    {
        static void Main(string[] args) {
            char a = '안';
            char b = '녕';
            char c = '하';
            char d = '세';
            char e = '요';

            // Write() 메서드는 줄바꿈이 일어나지 않음
            Console.Write(a);
            Console.Write(b);
            Console.Write(c);
            Console.Write(d);
            Console.WriteLine(e);
        }
    }
}
